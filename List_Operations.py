#List Operations in Python

print("               List Operations             ")
print("------------------------------------------------")
print("\n") 

L1=[10,20,30]
L2=[40,50,60]

print("List1:", L1)
print("List2:", L2)
print("\n")

print("L1[0]:", L1[0])
print("\n")

print("L2[-1]:", L2[-1])
print("\n")

print("L1[0:2]:", L1[0:2])
print("\n")

print("L1+L2:", L1 + L2)
print("\n")

print("L1*2:", L1 * 2)
print("\n")

print("10 in L1:", 10 in L1)
print("\n")

print("Length of L1:", len(L1))
print("Length of L2:", len(L2))
print("\n")

print("Max of L1:", max(L1))
print("Max of L2:", max(L2))
print("\n")

print("Min of L2:",min(L1))
print("\n")

print("Sum of L2:",sum(L1))